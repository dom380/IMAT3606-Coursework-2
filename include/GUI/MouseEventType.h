#pragma once
#ifndef EVENTTYPE_H
#define EVENTTYPE_H
/*
	Enum specifying the type of Mouse Event.
	LEFT_CLICK == Left mouse button clicked.
	RIGHT_CLICK == Right mouse button clicked.
	MOUSE_MOVE == Mouse pointer was moved.
*/
enum class MouseEventType {
	LEFT_CLICK,
	RIGHT_CLICK,
	MOUSE_MOVE
};

#endif // !EVENTTYPE_H


